// Interface language detection and text definitions
export const detectSystemLanguage = (): 'zh' | 'en' => {
  if (typeof window === 'undefined') return 'en'
  const lang = navigator.language.toLowerCase()
  return lang.startsWith('zh') ? 'zh' : 'en'
}

